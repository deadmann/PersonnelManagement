public class CopyRightInfo {
    private String yearEnglish;
    private String nameEnglish;
    private String yearPersian;
    private String namePersian;

    public String getYearEnglish() {
        return yearEnglish;
    }

    public void setYearEnglish(String yearEnglish) {
        this.yearEnglish = yearEnglish;
    }

    public String getNameEnglish() {
        return nameEnglish;
    }

    public void setNameEnglish(String nameEnglish) {
        this.nameEnglish = nameEnglish;
    }

    public String getYearPersian() {
        return yearPersian;
    }

    public void setYearPersian(String yearPersian) {
        this.yearPersian = yearPersian;
    }

    public String getNamePersian() {
        return namePersian;
    }

    public void setNamePersian(String namePersian) {
        this.namePersian = namePersian;
    }
}
